console.log(10 >5);// return true ou false
console.log(10 >= 11);
console.log(10 < 11);
console.log(10<=9);
console.log(10 == '10');// esta comparando number com String true
console.log(10 === '10');// comparando number com string não pode ser true é  false 
console.log(10 != '10');//esta comparando number com String era para ser true
console.log(10 !== '10');//comparando number com string true é diferente 


